package com.recipetime.find.Model;

public class Ingredients {
	private int ingorder;
	private String ingname;
	private int ingquantity;
	private String unit;
	private String exp;
	
	public int getIngorder() {
		return ingorder;
	}
	public void setIngorder(int ingorder) {
		this.ingorder = ingorder;
	}
	public String getIngname() {
		return ingname;
	}
	public void setIngname(String ingname) {
		this.ingname = ingname;
	}
	public int getIngquantity() {
		return ingquantity;
	}
	public void setIngquantity(int ingquantity) {
		this.ingquantity = ingquantity;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getExp() {
		return exp;
	}
	public void setExp(String exp) {
		this.exp = exp;
	}
	
	
}
